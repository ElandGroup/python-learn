fruits = ['apple','pear','orange']

print(fruits[0])
print(fruits[-1])

fruits.append('grape')
print(fruits)
fruits.remove("grape")
print(fruits)

fruits.insert(0,"banana")
print(fruits)

del fruits[0]
print(fruits)

fruit = fruits.pop(0)
print(fruit)
print(fruits)

# ==* ==*
fruits.sort()
print(fruits)

fruits.sort(reverse=True)
print(fruits)

fruits.reverse()
print(fruits)

print(len(fruits))

